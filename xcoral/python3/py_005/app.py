def charAt(phrase, idx):
    """
    Return element of a phrase at index position idx.

    Parameters
    ----------
    phrase : str
        String array.
    idx : int
        Index position.

    Returns
    -------
    str
        Element at index position idx.

    """
    return phrase[idx]

def indexOf(phrase, element):
    return phrase.index(element)

def returnX(x):
    return x

def numberAdd(x, y):
    return x + y

def numberSub(x, y):
    return x - y

def numberDiv(x, y):
    return x / y

def numberMul(x, y):
    return x * y
