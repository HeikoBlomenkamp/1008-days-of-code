def numberAbs(x):
    """
    Return the absolute value of a number.

    Parameters
    ----------
    x : _T
        Argument.

    Returns
    -------
    _T
        Absolute value.

    """
    return abs(x)

def numberPow(x, y):
    return pow(x, y)

def numberMax(x, y):
    return max(x, y)

def numberMin(x, y):
    return min(x, y)

def numberRound(x):
    return round(x)
